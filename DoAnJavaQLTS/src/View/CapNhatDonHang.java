    private void TableSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSPMouseClicked
        selectedIndex=TableSP.getSelectedRow();
        sanpham sp=sanpham.get(selectedIndex);
        txtMasp.setText(sp.getMasp());
        txtTensp.setText(sp.getTensp());
        ComboxKind.setSelectedItem(sp.getLoai());
        txtGia.setText(sp.getGia()+"");
       
        
    }//GEN-LAST:event_TableSPMouseClicked

    private void btnThemsanphamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemsanphamActionPerformed
        String Masp="";
        int soluong=0;
        int gia=0;
        Masp=txtTensp.getText();
        soluong=Integer.parseInt(txtSoluong.getText());
        gia=Integer.parseInt(txtGia.getText());
        int Gia=soluong * gia;
        SanphamCT sp=new SanphamCT(Masp,soluong,Gia);
        new DAOspCT().AddSP(sp);
        showTableSPCT();
        
        
        
    }//GEN-LAST:event_btnThemsanphamActionPerformed

    private void TableSPCTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSPCTMouseClicked
        selectedIndex=TableSPCT.getSelectedRow();
        SanphamCT sp=sanphamct.get(selectedIndex);
        new DAOspCT().DeleteSP(sp.getId());
        showTableSPCT(); 
    }//GEN-LAST:event_TableSPCTMouseClicked

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String DateOrder="";
        int gia=0;
        for(int i=0;i<TableSPCT.getRowCount();i++){
            gia+=Integer.parseInt(TableSPCT.getValueAt(i,3).toString());
            
        }
        DateOrder=txtDateOrder.getText();
        Hoadon hd=new Hoadon(DateOrder,gia);
        new DAOhd().AddHD(hd);
        showTableHD();
        JOptionPane.showMessageDialog(this, "Thêm hóa đơn thành công");
    }//GEN-LAST:event_btnLuuActionPerformed

