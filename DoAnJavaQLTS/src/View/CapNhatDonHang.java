private void showTableHD() 
    {
        hoadon =new DAOhd().getListHD();
        ModelHD.setRowCount(0);
        for(Hoadon hd:hoadon)
        {         
                ModelHD.addRow(new Object[]
                {
                     TableHoadon.getRowCount()+1,hd.getDateOrder(),hd.getThanhtien()
                });          
                   
        }
    }
private void showTableSP()  
    {
        sanpham=new DAOsp().getListSP();
        ModelSP.setRowCount(0);
        for(sanpham sp:sanpham)
        {         
                ModelSP.addRow(new Object[]
                {
                     TableSP.getRowCount()+1,sp.getMasp(),sp.getTensp(),sp.getLoai(),sp.getTrangthai(),sp.getGia()
                });          
                   
        }
    }
private void showTableSPCT()  
    {
        sanphamct=new DAOspCT().getListSP();
        ModelSPCT.setRowCount(0);
        for(SanphamCT sp:sanphamct)
        {         
                ModelSPCT.addRow(new Object[]
                {
                     TableSPCT.getRowCount()+1,sp.getMasp(),sp.getSoluong(),sp.getGia()
                });          
                   
        }
    }
