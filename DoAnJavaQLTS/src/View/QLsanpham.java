    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        selectedIndex=TableResult.getSelectedRow();
        sanpham n=sanpham.get(selectedIndex);
        new DAOsp().DeleteSP(n.getId());
        if(sanpham.isEmpty()){
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để sửa");
        }else if( selectedIndex==-1){
             JOptionPane.showMessageDialog(this, "Hãy chọn một dòng rồi sửa");
        }else{
             sanpham sp=new sanpham();
       
        sp.setMasp(txtMasp.getText());
        sp.setTensp(txtTensp.getText());
        sp.setLoai(ComboxL.getSelectedItem().toString());
        sp.setTrangthai(ComboxTT.getSelectedItem().toString());      
        
        boolean isOK=true;
        try{
            sp.setGia(Integer.parseInt(txtGia.getText()));            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Thêm không thành công - Không hợp lệ");
            isOK=false;
        }
        if(isOK)
        {
            new DAOsp().AddSP(sp);
            showTable();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void TableResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableResultMouseClicked
        selectedIndex=TableResult.getSelectedRow();
        sanpham sp= sanpham.get(selectedIndex);
        txtMasp.setText(sp.getMasp());
        txtTensp.setText(sp.getTensp());
        ComboxL.setSelectedItem(sp.getLoai());
        ComboxTT.setSelectedItem(sp.getTrangthai());
        txtGia.setText(sp.getGia()+"");
       
        
    }//GEN-LAST:event_TableResultMouseClicked

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String NAME=txtTensp.getText();
        if(NAME.length()>0){
            sanpham=new DAOsp().findbyName(NAME);
            Model.setRowCount(0);
            for(sanpham sp:sanpham){
               Model.addRow(new Object[]
                {
                     TableResult.getRowCount()+1,sp.getMasp(),sp.getTensp(),sp.getLoai(),sp.getTrangthai(),sp.getGia()
                });  
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

