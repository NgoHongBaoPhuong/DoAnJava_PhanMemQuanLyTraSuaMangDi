    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        sanpham sp=new sanpham();
        sp.setMasp(txtMasp.getText());
        sp.setTensp(txtTensp.getText());
        sp.setLoai(ComboxL.getSelectedItem().toString());
        sp.setTrangthai(ComboxTT.getSelectedItem().toString());      
        
        boolean isOK=true;
        try{
            sp.setGia(Integer.parseInt(txtGia.getText()));            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Thêm không thành công - Không hợp lệ");
            isOK=false;
        }
        if(isOK)
        {
            new DAOsp().AddSP(sp);
            showTable();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
    }//GEN-LAST:event_btnThemActionPerformed

